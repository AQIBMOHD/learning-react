// Generate menu response in Swiggy API format
const getMenuData = (restaurantId) => {
  const menu = menus[restaurantId];
  
  if (!menu) {
    // Return a generic menu for unknown restaurant IDs
    const genericMenu = menus["1001"]; // Use Pizza Hut as default
    return formatMenuResponse(restaurantId, {
      ...genericMenu,
      restaurantInfo: {
        ...genericMenu.restaurantInfo,
        name: `Restaurant #${restaurantId}`,
        id: restaurantId
      }
    });
  }
  
  return formatMenuResponse(restaurantId, menu);
};

// Format menu data to match Swiggy API structure
const formatMenuResponse = (restaurantId, menuData) => {
  return {
    data: {
      cards: [
        {
          card: {
            card: {
              "@type": "type.googleapis.com/swiggy.presentation.food.v2.Restaurant",
              info: {
                id: restaurantId,
                ...menuData.restaurantInfo
              }
            }
          }
        },
        {}, // Empty card (matches Swiggy structure)
        {
          groupedCard: {
            cardGroupMap: {
              REGULAR: {
                cards: menuData.categories.map((category) => ({
                  card: {
                    card: {
                      "@type": "type.googleapis.com/swiggy.presentation.food.v2.ItemCategory",
                      title: category.title,
                      itemCards: category.items.map((item) => ({
                        card: {
                          "@type": "type.googleapis.com/swiggy.presentation.food.v2.Dish",
                          info: {
                            id: item.id,
                            name: item.name,
                            price: item.price,
                            defaultPrice: item.price,
                            description: item.description,
                            imageId: item.imageId || "",
                            isVeg: item.isVeg,
                            ratings: {
                              aggregatedRating: {
                                rating: (3.8 + Math.random() * 0.7).toFixed(1),
                                ratingCount: Math.floor(Math.random() * 100) + 10 + " ratings"
                              }
                            }
                          }
                        }
                      }))
                    }
                  }
                }))
              }
            }
          }
        }
      ]
    }
  };
};

